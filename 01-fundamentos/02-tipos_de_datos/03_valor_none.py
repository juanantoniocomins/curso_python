# None es un valor especial en Python que representa la ausencia de valor.
# NO es lo mismo que 0, una cadena vacía (""), o False.
# Su tipo de dato es NoneType.

## 1. Declaración e Inicialización

# Puedes inicializar una variable a None para indicar que aún no tiene un valor útil.
valor_pendiente = None
print(f"1. Valor de la variable inicializada: {valor_pendiente}")

# Verifica el tipo de dato:
print(f"2. Tipo de 'valor_pendiente': {type(valor_pendiente)}")
print("-" * 30)