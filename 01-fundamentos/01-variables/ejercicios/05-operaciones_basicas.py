# Realiza operaciones básicas entre dos números.

numero1 = 12
numero2 = 6

print(f"{numero1} + {numero2} = {numero1+numero2}")
print(f"{numero1} - {numero2} = {numero1-numero2}")
print(f"{numero1} x {numero2} = {numero1*numero2}")
print(f"{numero1} / {numero2} = {numero1/numero2}")
print(f"{numero1} // {numero2} = {numero1 // numero2}") # división entera
print(f"{numero1} % {numero2} = {numero1%numero2}") # modulo, resto división
print(f"{numero1} ** {numero2} = {numero1**numero2}") # potencia